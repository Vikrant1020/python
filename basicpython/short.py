# short tags

# \" to add a new string in it
g="my\"name\" VIKRANT"
print(g)

# to use ' use \tage
a='it\'s my home'
print(a)

# to use () use \\ tage
b='my name is VIKRANT \\(ANKUR)'
print(b)

# for new line use \n
c="ankur \n vikarnt"
print(c)

# for tab space use \t
c="ankur\tvikrant"
print(c)

# removing the space
d="ankur\bvikarnt"
print(d)

